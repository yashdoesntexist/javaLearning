package javaLearning;

public class LoopClass {
    public static void main(String[] args) {
        int j = 11;
        // loops
        // for loops
        for(int i = 0; i <= 10; i++){
            System.out.println(i);
        }
        // while loop
        while (j <= 20) {
            System.out.println(j);
            j++;
            
        }
        // do-while loop
        int k =21;
        do{
            System.out.println(k);
            k++;
        }while(k <=30);
    }
}
